    public void setModel(){
        String[] cabecera={"Nro","Receta","Detalle","Fecha"};
        modelo.setColumnIdentifiers(cabecera);
         tblcambios.setModel(modelo);
    
    }  
   public void setDatos(){
       Object[] fila = new Object[modelo.getColumnCount()];
   int nro=1;
   for( AuditarM datos : listaAuditoria ){
       fila[0]=nro;
       fila[1]=datos.getReceta();
       fila[2]=datos.getDetalle();
       fila[3]=datos.getFecha();
     
       nro++;
       modelo.addRow(fila);
       
       
   }
   }

    
    
    
    
    
